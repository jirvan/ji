package com.jirvan.lang;

/** The only real purpose if this exception is to provide an exception class
 *  where it is clear that the primary purpose of the exception is to
 *  communicate a message without regard to the stack trace type info (i.e. to
 *  let higher level exception handlers know they should display the message
 *  and not confuse the issue with stack trace type stuff.  Generally this
 *  means that it is a "business rule" type exception rather than a program
 *  error.
 */
public class MessageException extends RuntimeException {

    public MessageException() {
    }

    public MessageException(String message) {
        super(message);
    }

    public MessageException(String message, Throwable cause) {
        super(message, cause);
    }

    public MessageException(Throwable cause) {
        super(cause);
    }

}
